import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const DATA_DIR = path.join(ROOT, "js", "data");

const SUPERSCRIPT_TO_DIGIT = {
  "\u2070": "0", "\u00b9": "1", "\u00b2": "2", "\u00b3": "3", "\u2074": "4",
  "\u2075": "5", "\u2076": "6", "\u2077": "7", "\u2078": "8", "\u2079": "9",
};

const NOBLE_GAS_EXPANDED = {
  He: "1s\u00b2",
  Ne: "1s\u00b2 2s\u00b2 2p\u2076",
  Ar: "1s\u00b2 2s\u00b2 2p\u2076 3s\u00b2 3p\u2076",
  Kr: "1s\u00b2 2s\u00b2 2p\u2076 3s\u00b2 3p\u2076 3d\u2071\u2070 4s\u00b2 4p\u2076",
  Xe: "1s\u00b2 2s\u00b2 2p\u2076 3s\u00b2 3p\u2076 3d\u2071\u2070 4s\u00b2 4p\u2076 4d\u2071\u2070 5s\u00b2 5p\u2076",
  Rn: "1s\u00b2 2s\u00b2 2p\u2076 3s\u00b2 3p\u2076 3d\u2071\u2070 4s\u00b2 4p\u2076 4d\u2071\u2070 5s\u00b2 5p\u2076 4f\u2071\u2074 5d\u2071\u2070 6s\u00b2 6p\u2076",
};

function superscriptToInt(str) {
  if (!str) return 0;
  const normalized = str.replace(/[\u2070\u00b9\u00b2\u00b3\u2074\u2075\u2076\u2077\u2078\u2079]/g, (ch) => SUPERSCRIPT_TO_DIGIT[ch] ?? ch);
  const n = parseInt(normalized, 10);
  return Number.isFinite(n) ? n : 0;
}

function expandNobleGasShorthand(config) {
  let expanded = config.trim();
  for (const [symbol, inner] of Object.entries(NOBLE_GAS_EXPANDED)) {
    expanded = expanded.replace(new RegExp(`\\[${symbol}\\]`, "g"), inner);
  }
  return expanded;
}

function parseShellArrangementFromConfig(config) {
  if (!config || typeof config !== "string") return [];

  const full = expandNobleGasShorthand(
    config.replace(/\[[^\]]*\]/g, (match) => {
      const inner = match.slice(1, -1);
      if (NOBLE_GAS_EXPANDED[inner]) return NOBLE_GAS_EXPANDED[inner];
      return "";
    }).replace(/\s+/g, " ").trim(),
  );

  const shellCounts = {};
  const orbitalRe = /(\d)([spdf])([\u2070\u00b9\u00b2\u00b3\u2074\u2075\u2076\u2077\u2078\u2079]+|\d+)/g;
  let match;
  while ((match = orbitalRe.exec(full)) !== null) {
    const n = parseInt(match[1], 10);
    const count = superscriptToInt(match[3]);
    if (n > 0 && count > 0) {
      shellCounts[n] = (shellCounts[n] || 0) + count;
    }
  }

  const maxN = Math.max(0, ...Object.keys(shellCounts).map(Number));
  if (maxN === 0) return [];

  const out = [];
  for (let n = 1; n <= maxN; n++) {
    out.push(shellCounts[n] || 0);
  }
  return out;
}

function serialize(value, indent = 0) {
  const pad = "  ".repeat(indent);
  const padIn = "  ".repeat(indent + 1);
  if (value === null) return "null";
  if (typeof value === "string") return JSON.stringify(value);
  if (typeof value === "number" || typeof value === "boolean") return String(value);
  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    const items = value.map((item) => `${padIn}${serialize(item, indent + 1)}`);
    return `[\n${items.join(",\n")}\n${pad}]`;
  }
  const entries = Object.entries(value);
  if (entries.length === 0) return "{}";
  const items = entries.map(([key, val]) => {
    const keyStr = /^[A-Za-z_$][\w$]*$/.test(key) ? key : JSON.stringify(key);
    return `${padIn}${keyStr}: ${serialize(val, indent + 1)}`;
  });
  return `{\n${items.join(",\n")}\n${pad}}`;
}

async function main() {
  const { finallyData } = await import("../js/data/elementsDetail.js");
  const { elements } = await import("../js/data/elementsIndex.js");

  const enrichedElements = elements.map((element) => {
    const detail = finallyData[String(element.number)] || {};
    const mass = detail.level2_atomic?.mass?.highSchool || "";
    if (!mass) return element;
    return { ...element, mass };
  });

  const shellsByZ = {};
  for (const [key, entry] of Object.entries(finallyData)) {
    const config = entry?.level3_properties?.electronic?.configuration || "";
    shellsByZ[key] = parseShellArrangementFromConfig(config);
  }

  const header = "// Auto-generated by scripts/generate-elements-data.mjs — do not edit manually.\n";
  const headerSource = "// Electron affinity added for educational display.\n// Source strategy: broad table values from PeriodicTable.com / Wolfram ElementData; missing values kept as N/A.\n\n";

  writeFileSync(
    path.join(DATA_DIR, "elementsDetail.js"),
    `${header}${headerSource}export const finallyData = ${serialize(finallyData)};\n`,
    "utf8",
  );

  writeFileSync(
    path.join(DATA_DIR, "elementsIndex.js"),
    `${header}export const elements = ${serialize(enrichedElements)};\n`,
    "utf8",
  );

  writeFileSync(
    path.join(DATA_DIR, "elementShells.js"),
    `${header}export const shellsByZ = ${serialize(shellsByZ)};\n`,
    "utf8",
  );

  writeFileSync(
    path.join(DATA_DIR, "elementsData.js"),
    `${header}// Backward-compatible barrel — prefer elementsIndex / elementsDetail for tree-shaking.\nexport { elements } from "./elementsIndex.js";\nexport { finallyData } from "./elementsDetail.js";\n`,
    "utf8",
  );

  console.log(`Generated elementsDetail.js, elementsIndex.js (${enrichedElements.length} elements), elementShells.js, and elementsData.js barrel.`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
